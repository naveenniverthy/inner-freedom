const moduleDefinitions = [
  { name: "Foundation", startDay: 1, endDay: 3 },
  { name: "Inquiry", startDay: 4, endDay: 8 },
  { name: "Understanding", startDay: 9, endDay: 13 },
  { name: "Living Knowledge", startDay: 14, endDay: 19 },
];

function moduleForLesson(lesson) {
  if (lesson.isOptional) {
    return "Optional Reference";
  }

  const match = moduleDefinitions.find(
    (module) => lesson.day >= module.startDay && lesson.day <= module.endDay
  );

  return match?.name ?? lesson.module ?? "";
}

function normalizeLesson(lesson) {
  const body =
    Array.isArray(lesson.body) && lesson.body.length > 0
      ? lesson.body
      : lesson.content
        ? lesson.content
            .split(/\n\s*\n/)
            .map((paragraph) => paragraph.trim())
            .filter(Boolean)
        : [];

  return {
    verse: "",
    content: "",
    keyInsight: "",
    commonMisunderstanding: "",
    takeaway: "",
    reflection: "",
    closing: "",
    note: "",
    takeawayLine: "",
    ...lesson,
    module: moduleForLesson(lesson),
    body,
    content: lesson.content || body.join("\n\n"),
  };
}

export const lessons = rawLessons.map(normalizeLesson);

export const mainLessons = lessons.filter((lesson) => !lesson.isOptional);

export const optionalLessons = lessons.filter((lesson) => lesson.isOptional);

export const modules = moduleDefinitions.map((module) => {
  const moduleLessons = mainLessons.filter(
    (lesson) => lesson.day >= module.startDay && lesson.day <= module.endDay
  );

  return {
    name: module.name,
    startDay: module.startDay,
    endDay: module.endDay,
    lessonCount: moduleLessons.length,
    lessons: moduleLessons,
  };
});

export function getLessonBySlug(slug) {
  return lessons.find((lesson) => lesson.slug === slug);
}

export function getLessonIndex(slug) {
  return lessons.findIndex((lesson) => lesson.slug === slug);
}
