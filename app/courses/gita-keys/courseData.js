export const allLessons = sections.flatMap((section) =>
  section.lessons.map((lesson, lessonIndex) => ({
    ...lesson,
    sectionKey: section.key,
    sectionTitle: section.title,
    sectionSubtitle: section.subtitle,
    sectionLessonIndex: lessonIndex + 1,
    sectionLessonTotal: section.lessons.length,
    href: `/courses/${courseSlug}/${section.key}/${lesson.slug}`
  }))
);

function resolveLesson(targetOrSectionKey, lessonSlug) {
  if (lessonSlug) {
    return allLessons.find(
      (lesson) =>
        lesson.sectionKey === targetOrSectionKey && lesson.slug === lessonSlug
    );
  }

  return allLessons.find(
    (lesson) =>
      lesson.href === targetOrSectionKey ||
      `${lesson.sectionKey}/${lesson.slug}` === targetOrSectionKey
  );
}

export function getPreviousLesson(targetOrSectionKey, lessonSlug) {
  const lesson = resolveLesson(targetOrSectionKey, lessonSlug);
  const index = lesson ? allLessons.findIndex((item) => item.href === lesson.href) : -1;

  return index > 0 ? allLessons[index - 1] : null;
}

export function getNextLesson(targetOrSectionKey, lessonSlug) {
  const lesson = resolveLesson(targetOrSectionKey, lessonSlug);
  const index = lesson ? allLessons.findIndex((item) => item.href === lesson.href) : -1;

  return index >= 0 && index < allLessons.length - 1 ? allLessons[index + 1] : null;
}

export function getTotalLessonCount() {
  return allLessons.length;
}

export function getLessonIndex(targetOrSectionKey, lessonSlug) {
  const lesson = resolveLesson(targetOrSectionKey, lessonSlug);
  const index = lesson ? allLessons.findIndex((item) => item.href === lesson.href) : -1;

  return index >= 0 ? index + 1 : 0;
}

export function getSection(sectionKey) {
  return sections.find((section) => section.key === sectionKey);
}

export function getLesson(sectionKey, lessonSlug) {
  return resolveLesson(sectionKey, lessonSlug);
}
