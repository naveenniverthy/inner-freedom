export function getStepBySlug(slug) {
  return steps.find((step) => step.slug === slug);
}

export function getStageBySlug(slug) {
  return stages.find((stage) => stage.slug === slug);
}

export function getStageForStep(number) {
  return stages.find((stage) => number >= stage.range[0] && number <= stage.range[1]);
}

export function getStepsForStage(stageSlug) {
  const stage = getStageBySlug(stageSlug);

  if (!stage) {
    return [];
  }

  return steps.filter(
    (step) => step.number >= stage.range[0] && step.number <= stage.range[1]
  );
}

export function getPreviousStep(number) {
  return steps.find((step) => step.number === number - 1) ?? null;
}

export function getNextStep(number) {
  return steps.find((step) => step.number === number + 1) ?? null;
}
